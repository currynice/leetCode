    public int majorityElement(int[] nums) {

        int can_num = nums[0];
        int count = 1;
        for(int i=1;i<nums.length;i++){
            if(count==0){
                can_num = nums[i];
                count =1;
                continue;
            }

            if(nums[i]==can_num){
                count = count+1;
            }else {
                count = count-1;
            }
        }

        return can_num;

    }
